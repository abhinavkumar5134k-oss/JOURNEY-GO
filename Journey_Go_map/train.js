async function getTrainSchedule() {
    const trainNo = document.getElementById("trainNo").value.trim();

    if (!trainNo) {
        alert("Enter Train Number");
        return;
    }

    // Checking our local database object
    const trainData = localTrainDatabase[trainNo];

    if (!trainData) {
        alert(`ℹ️ Train ${trainNo} is not added to the local data system yet.`);
        return;
    }

    // Clear previous dashboard items
    const tbody = document.querySelector("#scheduleTable tbody");
    tbody.innerHTML = "";

    // Set Header Card
    document.getElementById("trainInfo").innerHTML = `
        <strong>Train Number:</strong> ${trainData.train_number} <br>
        <strong>Train Name:</strong> ${trainData.train_name}
    `;

    const stations = trainData.route || [];

    if (stations.length === 0) {
        tbody.innerHTML = `<tr><td colspan="7">No route schedule available.</td></tr>`;
        return;
    }

    // Build table rows smoothly
    let tableRows = "";
    stations.forEach((station, index) => {
        tableRows += `
            <tr>
                <td>${index + 1}</td>
                <td>${station.station_name || "-"}</td>
                <td>${station.station_code || "-"}</td>
                <td>${station.sta || "-"}</td>
                <td>${station.std || "-"}</td>
                <td>${station.day || "1"}</td>
                <td>${station.distance || "0"}</td>
            </tr>
        `;
    });

    tbody.innerHTML = tableRows;
}